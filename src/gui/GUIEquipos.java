    private void jComboBox1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jComboBox1KeyPressed
        if(evt.getKeyCode() == KeyEvent.VK_ENTER){
			try {
				List<Equipo> le = MGUI.con.getEquiposByNombre(jComboBox1.getSelectedItem().toString());
				if(le.isEmpty()){
					int op = MGUI.notifica(this, "No existe el equipo", null, "El equipo no existe, Â¿desea crearlo?", JOptionPane.INFORMATION_MESSAGE);
					if(op == JOptionPane.YES_OPTION){
						Equipo e = new Equipo(jComboBox1.getSelectedItem().toString());
						selectedEquipo = e;
					}
				}
			} catch (SQLException ex) {
				Logger.getLogger(GUIEquipos.class.getName()).log(Level.SEVERE, null, ex);
			}
		}
    }//GEN-LAST:event_jComboBox1KeyPressed

